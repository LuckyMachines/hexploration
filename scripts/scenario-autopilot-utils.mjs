const DEFAULT_LIMITS = {
  maxIterations: 2,
  timeoutMs: 180_000,
  targetScore: 70,
  targetConfidence: 0.68,
  maxCandidates: 4,
  allowedFiles: ['simulator.balance.json', 'simulator.scenarios.json'],
};

export function autopilotLimits(input = {}) {
  return {
    ...DEFAULT_LIMITS,
    ...input,
    maxIterations: Math.max(0, Number(input.maxIterations ?? input.iterations ?? DEFAULT_LIMITS.maxIterations)),
    timeoutMs: Math.max(10_000, Number(input.timeoutMs ?? DEFAULT_LIMITS.timeoutMs)),
    targetScore: Math.max(1, Math.min(100, Number(input.targetScore ?? DEFAULT_LIMITS.targetScore))),
    targetConfidence: Math.max(0.1, Math.min(0.95, Number(input.targetConfidence ?? DEFAULT_LIMITS.targetConfidence))),
    maxCandidates: Math.max(1, Number(input.maxCandidates ?? DEFAULT_LIMITS.maxCandidates)),
  };
}

export function now() {
  return new Date().toISOString();
}

export function clone(value) {
  return JSON.parse(JSON.stringify(value ?? null));
}

function titleCase(value) {
  return String(value || '')
    .split(/[-_\s]+/)
    .filter(Boolean)
    .map((part) => `${part.slice(0, 1).toUpperCase()}${part.slice(1)}`)
    .join(' ');
}

export function normalizeAutopilotIntent(text = '', options = {}) {
  const source = String(text || options.intent || '').trim();
  const fallback = options.id ? `Improve scenario ${options.id}` : 'Make the game feel alive, readable, and tunable.';
  const intent = source || fallback;
  const lower = intent.toLowerCase();
  const qualities = [];
  for (const [key, pattern] of Object.entries({
    tense: /tense|desperate|pressure|danger|survival/,
    fair: /fair|readable|clear|legible|not random|not hopeless/,
    cooperative: /cooperative|cooperation|help|crew|together|shared/,
    rewarding: /rewarding|payoff|artifact|relic|treasure/,
    alive: /alive|responsive|physical|feel|fun/,
    surprising: /surprising|surprise|volatile|chaos|random/,
  })) {
    if (pattern.test(lower)) qualities.push(key);
  }
  return {
    text: intent,
    slug: slugify(options.id || intent),
    qualities: qualities.length > 0 ? qualities : ['alive'],
    requestedScenarioId: options.id || '',
    createdAt: now(),
  };
}

export function uniqueAutopilotScenarioId(baseId, store = loadScenarioStore()) {
  const base = slugify(baseId.startsWith('autopilot-') ? baseId : `autopilot-${baseId}`);
  if (!findScenario(store, base)) return base;
  let index = 2;
  while (findScenario(store, `${base}-${index}`)) index += 1;
  return `${base}-${index}`;
}

function enrichScenarioForAutopilot(scenario, intent, options = {}) {
  const normalized = normalizeScenario({
    ...scenario,
    id: scenario.id,
    notes: {
      ...(scenario.notes || {}),
      autopilotIntent: intent.text,
      hypothesis: scenario.notes?.hypothesis || intent.text,
    },
    batch: Number(options.batch || scenario.batch || (scenario.players >= 4 ? 2 : 3)),
    seed: options.seed || scenario.seed || stableSeed(scenario.id),
    setupForge: scenario.setupForge || parseScenarioIntent(intent.text, { id: scenario.id }).setupForge,
  });
  return {
    ...normalized,
    setupForge: normalizeSetupForge(normalized.setupForge || {}, normalized),
  };
}

export function resolveAutopilotScenario({ intentText = '', id = '', store = loadScenarioStore(), save = false, options = {} } = {}) {
  const intent = normalizeAutopilotIntent(intentText, { id });
  const existing = id ? findScenario(store, id) : null;
  const scenario = existing
    ? enrichScenarioForAutopilot(existing, intent, options)
    : enrichScenarioForAutopilot(parseScenarioIntent(intent.text, {
      id: uniqueAutopilotScenarioId(intent.slug, store),
      name: titleCase(intent.slug),
      note: 'Generated by Scenario Autopilot.',
      batch: options.batch,
      turns: options.turns,
      players: options.players,
      seed: options.seed,
    }), intent, options);
  const setupValidation = validateSetupForge(scenario.setupForge || {}, scenario, options.setupMode || 'best-effort');
  if (save) saveScenarioStore(upsertScenario(store, scenario));
  return {
    intent,
    scenario,
    setupForge: setupValidation.setupForge,
    setupValidation,
    persisted: Boolean(save),
  };
}

export function loadBalance(path = balancePath) {
  return readJson(path, { schemaVersion: 1, knobs: {}, gates: {} });
}

function knob(balance, key, fallback) {
  return Number(balance.knobs?.[key] ?? fallback);
}

function clamp(value, min, max) {
  return Math.max(min, Math.min(max, Number(value)));
}

function patchCandidate(input) {
  return {
    risk: 'low',
    verificationCommand: 'npm run autopilot:dry',
    rollbackPlan: 'Autopilot restores JSON snapshots when a candidate is rejected.',
    ...input,
  };
}

function weakestScore(oracle = {}) {
  return Object.entries(oracle.experienceScores || {})
    .sort(([, a], [, b]) => Number(a.score || 0) - Number(b.score || 0))[0] || ['unknown', { score: 0 }];
}

export function summarizeAutopilotEvidence(report = {}, oracle = null, scenario = {}) {
  const verdict = report.scenarioVerdict || (report.turns ? evaluateScenarioReport(report, scenario) : null);
  const weakest = weakestScore(oracle || report.oracle || {});
  return {
    oracleVerdict: oracle?.oracleVerdict || report.oracle?.oracleVerdict || 'not-run',
    weightedScore: Number(oracle?.weightedScore ?? report.oracle?.weightedScore ?? 0),
    confidence: Number(oracle?.confidence ?? report.oracle?.confidence ?? 0),
    weakestMetric: weakest[0],
    weakestScore: Number(weakest[1]?.score || 0),
    setupLevel: oracle?.setup?.level || report.setupLevel || 'none',
    requiredSetupLevel: oracle?.setup?.requiredSetupLevel || scenario.requiredSetupLevel || 'metadata',
    lifeScore: Number(report.funDebugger?.averageLifeScore || 0),
    flatTurnRate: Number(report.funDebugger?.flatTurnRate || 0),
    aliveTurnRate: Number(report.funDebugger?.aliveTurnRate || 0),
    invalidAttempts: Number(report.aggregate?.averages?.invalidAttempts || 0),
    zeroStatPlayers: Number(report.aggregate?.averages?.zeroStatPlayers || 0),
    artifacts: Number(report.aggregate?.averages?.artifacts || 0),
    revealedZones: Number(report.aggregate?.averages?.revealedZones || 0),
    scenarioVerdict: verdict?.verdict || 'unknown',
    failedTargets: (verdict?.targets || []).filter((target) => !target.pass).map((target) => target.label || target.metric),
    triggeredFailures: (verdict?.failureSignals || []).filter((signal) => signal.pass).map((signal) => signal.label || signal.metric),
    topIssue: report.funDebugger?.topIssue?.key || null,
    topIssueLabel: report.funDebugger?.topIssue?.label || null,
  };
}

export function diagnoseAutopilot({ scenario = {}, setupValidation = null, baselineReport = null, baselineOracle = null } = {}) {
  const evidence = baselineReport ? summarizeAutopilotEvidence(baselineReport, baselineOracle, scenario) : null;
  const lines = [];
  if (setupValidation?.warnings?.length) lines.push(`Setup has ${setupValidation.warnings.length} warning(s): ${setupValidation.warnings.slice(0, 2).join(' / ')}`);
  if (!baselineReport) lines.push('No baseline run has been captured yet.');
  if (evidence?.weakestMetric) lines.push(`Weakest Oracle dimension is ${evidence.weakestMetric} at ${Math.round(evidence.weakestScore)}.`);
  if (evidence?.topIssueLabel) lines.push(`Fun Debugger top issue is ${evidence.topIssueLabel}.`);
  if (evidence?.failedTargets?.length) lines.push(`Failed scenario targets: ${evidence.failedTargets.join(', ')}.`);
  if (evidence?.triggeredFailures?.length) lines.push(`Triggered failure signals: ${evidence.triggeredFailures.join(', ')}.`);
  if (evidence?.setupLevel === 'metadata' && evidence.requiredSetupLevel !== 'metadata') lines.push(`Required setup level is ${evidence.requiredSetupLevel}, but run only reached metadata.`);
  if (lines.length === 0) lines.push('Scenario evidence is healthy enough to test a smaller targeted iteration.');
  return { lines, evidence };
}

