export function generateAutopilotCandidates({ scenario = {}, baselineReport = null, baselineOracle = null, setupValidation = null, balance = loadBalance(), maxCandidates = 4 } = {}) {
  const evidence = baselineReport ? summarizeAutopilotEvidence(baselineReport, baselineOracle, scenario) : null;
  const weakest = evidence?.weakestMetric || 'setup';
  const candidates = [];
  const blocked = (setupValidation?.support || []).filter((field) => field.critical && ['contractBlocked', 'notYetSupported'].includes(field.status));
  if (blocked.length > 0 || (evidence?.setupLevel === 'metadata' && evidence?.requiredSetupLevel !== 'metadata')) {
    candidates.push(patchCandidate({
      id: `${scenario.id || 'scenario'}-setup-honesty`,
      title: 'Reduce setup dependence before balance tuning',
      hypothesis: 'The scenario should ask for only the setup level the engine can honestly apply before measured turns.',
      targetMetric: 'confidence',
      changeType: 'scenario-setup',
      risk: 'medium',
      files: ['simulator.scenarios.json'],
      patch: scenarioPatch(scenario, {
        requiredSetupLevel: 'partial',
        setupForge: {
          ...(scenario.setupForge || {}),
          board: {
            ...(scenario.setupForge?.board || {}),
            landingZone: '',
          },
          time: {
            ...(scenario.setupForge?.time || {}),
            day: null,
            queuePhase: '',
          },
        },
      }),
      verificationCommand: `npm run setup:validate -- --id=${scenario.id} && npm run scenario:run -- --id=${scenario.id}`,
    }));
  }
  if (!baselineReport) {
    candidates.push(patchCandidate({
      id: `${scenario.id || 'scenario'}-baseline`,
      title: 'Capture the baseline run',
      hypothesis: 'Autopilot needs one exact-engine run before it can recommend a grounded patch.',
      targetMetric: 'evidence',
      changeType: 'run',
      risk: 'low',
      files: [],
      patch: {},
      verificationCommand: scenario.id ? `npm run autopilot -- --id=${scenario.id} --mode=single-pass` : 'npm run autopilot -- "describe the scenario"',
    }));
    return candidates.slice(0, maxCandidates);
  }
  if (weakest === 'agency' || evidence?.topIssue === 'oneChoice') {
    candidates.push(patchCandidate({
      id: `${scenario.id}-agency-strategy-mix`,
      title: 'Broaden early action pressure',
      hypothesis: 'More move/dig pressure should create visible alternatives before turns collapse into passive repetition.',
      targetMetric: 'agency',
      changeType: 'scenario-design',
      files: ['simulator.scenarios.json'],
      patch: scenarioPatch(scenario, { strategies: uniqueStrategies(['move', 'dig', ...(scenario.strategies || []), 'balanced']), batch: Math.max(2, Number(scenario.batch || 1)) }),
      verificationCommand: `npm run scenario:run -- --id=${scenario.id} && npm run oracle:scenario -- --id=${scenario.id}`,
    }));
    candidates.push(patchCandidate({
      id: `${scenario.id}-agency-balance`,
      title: 'Bias movement without changing rules',
      hypothesis: 'A small movement bias can increase meaningful choices while staying inside simulator behavior knobs.',
      targetMetric: 'agency',
      changeType: 'balance-knob',
      files: ['simulator.balance.json'],
      patch: balancePatch({
        moveBias: knob(balance, 'moveBias', 1) + 1,
        movementFallbackPriority: 2,
        choiceDensityReward: knob(balance, 'choiceDensityReward', 18) + 3,
      }),
      verificationCommand: `npm run autopilot -- --id=${scenario.id} --mode=iterate --apply`,
    }));
  }
  if (weakest === 'readability' || evidence?.invalidAttempts > 0) {
    candidates.push(patchCandidate({
      id: `${scenario.id}-readability-guard`,
      title: 'Reject invalid-action noise harder',
      hypothesis: 'Readability improves when invalid attempts are more expensive to the tuning score and safer fallbacks are preferred.',
      targetMetric: 'readability',
      changeType: 'balance-knob',
      files: ['simulator.balance.json'],
      patch: balancePatch({
        invalidAttemptPenalty: knob(balance, 'invalidAttemptPenalty', 5) + 3,
        movementFallbackPriority: 2,
      }),
      verificationCommand: `npm run autopilot -- --id=${scenario.id} --mode=iterate --apply`,
    }));
  }
  if (weakest === 'tension' || scenario.tags?.includes('escape')) {
    candidates.push(patchCandidate({
      id: `${scenario.id}-controlled-pressure`,
      title: 'Add one setup prelude pressure beat',
      hypothesis: 'A discarded prelude can create a tenser measured start without distorting measured turn metrics.',
      targetMetric: 'tension',
      changeType: 'scenario-setup',
      risk: 'medium',
      files: ['simulator.scenarios.json'],
      patch: scenarioPatch(scenario, {
        setupForge: {
          ...(scenario.setupForge || {}),
          pressure: { ...(scenario.setupForge?.pressure || {}), escapePressure: scenario.tags?.includes('escape') || Boolean(scenario.setupForge?.pressure?.escapePressure), lowStatPressure: true },
          scriptedPrelude: { ...(scenario.setupForge?.scriptedPrelude || {}), turns: Math.max(1, Number(scenario.setupForge?.scriptedPrelude?.turns || 0)), strategies: ['risky', 'rest'], discardPreludeFromMetrics: true },
        },
      }),
      verificationCommand: `npm run scenario:run -- --id=${scenario.id} && npm run oracle:scenario -- --id=${scenario.id}`,
    }));
  }
  if (weakest === 'recovery' || evidence?.zeroStatPlayers > 0) {
    candidates.push(patchCandidate({
      id: `${scenario.id}-recovery-window`,
      title: 'Open the recovery window earlier',
      hypothesis: 'Recovery should appear before stat collapse, especially in cooperative pressure.',
      targetMetric: 'recovery',
      changeType: 'balance-knob',
      files: ['simulator.balance.json'],
      patch: balancePatch({
        recoverAtStat: clamp(knob(balance, 'recoverAtStat', 1) + 1, 1, 3),
        restBias: knob(balance, 'restBias', 1) + 1,
        statCollapsePenalty: knob(balance, 'statCollapsePenalty', 14) + 3,
      }),
      verificationCommand: `npm run autopilot -- --id=${scenario.id} --mode=iterate --apply`,
    }));
  }
  if (weakest === 'surprise' || scenario.tags?.includes('artifact')) {
    candidates.push(patchCandidate({
      id: `${scenario.id}-artifact-payoff`,
      title: 'Increase artifact payoff pressure',
      hypothesis: 'More dig pressure and artifact reward scoring should reveal whether artifact hunting can become the emotional peak.',
      targetMetric: 'surprise',
      changeType: 'balance-knob',
      files: ['simulator.balance.json'],
      patch: balancePatch({
        digBias: knob(balance, 'digBias', 1) + 1,
        artifactLifeReward: knob(balance, 'artifactLifeReward', 22) + 4,
      }),
      verificationCommand: `npm run autopilot -- --id=${scenario.id} --mode=iterate --apply`,
    }));
  }
  if (weakest === 'pacing' || evidence?.flatTurnRate > 0.35) {
    candidates.push(patchCandidate({
      id: `${scenario.id}-pacing-trim`,
      title: 'Shorten the proof window',
      hypothesis: 'A tighter turn count can expose the intended arc faster and reduce late flat turns.',
      targetMetric: 'pacing',
      changeType: 'scenario-design',
      files: ['simulator.scenarios.json'],
      patch: scenarioPatch(scenario, { turns: Math.max(6, Number(scenario.turns || 12) - 2), batch: Math.max(2, Number(scenario.batch || 1)) }),
      verificationCommand: `npm run scenario:run -- --id=${scenario.id} && npm run oracle:scenario -- --id=${scenario.id}`,
    }));
  }
  if (candidates.length === 0) {
    candidates.push(patchCandidate({
      id: `${scenario.id}-confidence-batch`,
      title: 'Raise evidence confidence',
      hypothesis: 'The run looks plausible; the smallest next step is a broader batch and strategy spread.',
      targetMetric: 'confidence',
      changeType: 'scenario-design',
      files: ['simulator.scenarios.json'],
      patch: scenarioPatch(scenario, { batch: Math.max(3, Number(scenario.batch || 1)), strategies: uniqueStrategies([...(scenario.strategies || []), 'balanced', 'move', 'risky']) }),
      verificationCommand: `npm run scenario:run -- --id=${scenario.id} && npm run oracle:scenario -- --id=${scenario.id}`,
    }));
  }
  return dedupeCandidates(candidates).slice(0, maxCandidates);
}

function uniqueStrategies(strategies) {
  const allowed = new Set(['balanced', 'risky', 'dig', 'move', 'rest', 'idle']);
  return [...new Set(strategies.filter((strategy) => allowed.has(strategy)))];
}

function dedupeCandidates(candidates) {
  const seen = new Set();
  return candidates.filter((candidate) => {
    if (seen.has(candidate.id)) return false;
    seen.add(candidate.id);
    return true;
  });
}

export function selectAutopilotCandidate(candidates = []) {
  const order = { run: 0, 'scenario-setup': 1, 'scenario-design': 2, 'balance-knob': 3 };
  return [...candidates].sort((a, b) => (order[a.changeType] ?? 9) - (order[b.changeType] ?? 9) || a.files.length - b.files.length)[0] || null;
}

function readText(path) {
  return existsSync(path) ? readFileSync(path, 'utf8') : '';
}

function writeStableJson(path, value) {
  writeJson(path, value);
}

function applyScenarioPatch(store, candidatePatch) {
  const scenarioId = candidatePatch.scenarioId;
  const current = findScenario(store, scenarioId);
  if (!current) throw new Error(`Cannot patch missing scenario ${scenarioId}`);
  const nextScenario = normalizeScenario(deepMerge(current, candidatePatch.patch || {}));
  return upsertScenario(store, nextScenario);
}

function deepMerge(base, patch) {
  if (!patch || typeof patch !== 'object' || Array.isArray(patch)) return clone(patch);
  const result = { ...(base || {}) };
  for (const [key, value] of Object.entries(patch)) {
    if (value && typeof value === 'object' && !Array.isArray(value)) result[key] = deepMerge(result[key] || {}, value);
    else result[key] = clone(value);
  }
  return result;
}

export function applyAutopilotCandidate(candidate, { storePath = resolve(root, 'simulator.scenarios.json'), balanceFile = balancePath } = {}) {
  if (!candidate) throw new Error('No candidate selected.');
  const snapshots = {};
  for (const file of candidate.files || []) {
    const path = candidateFilePath(file, { storePath, balanceFile });
    snapshots[file] = readText(path);
  }
  if (candidate.patch?.file === 'simulator.scenarios.json') {
    const store = readJson(storePath, loadScenarioStore());
    writeStableJson(storePath, applyScenarioPatch(store, candidate.patch));
  }
  if (candidate.patch?.file === 'simulator.balance.json') {
    const balance = readJson(balanceFile, loadBalance(balanceFile));
    writeStableJson(balanceFile, {
      ...balance,
      knobs: {
        ...(balance.knobs || {}),
        ...(candidate.patch.knobs || {}),
      },
    });
  }
  return {
    candidateId: candidate.id,
    files: candidate.files || [],
    snapshots,
    appliedAt: now(),
  };
}

function candidateFilePath(file, { storePath = resolve(root, 'simulator.scenarios.json'), balanceFile = balancePath } = {}) {
  if (file === 'simulator.scenarios.json') return storePath;
  if (file === 'simulator.balance.json') return balanceFile;
  return resolve(root, file);
}

export function rollbackAutopilotPatch(appliedPatch = {}, paths = {}) {
  for (const [file, content] of Object.entries(appliedPatch.snapshots || {})) {
    const path = candidateFilePath(file, paths);
    mkdirSync(dirname(path), { recursive: true });
    writeFileSync(path, content);
  }
  return {
    rolledBackAt: now(),
    files: Object.keys(appliedPatch.snapshots || {}),
  };
}

export function compareAutopilotRuns(baselineReport = {}, baselineOracle = {}, finalReport = {}, finalOracle = {}) {
  const before = summarizeAutopilotEvidence(baselineReport, baselineOracle, baselineReport.scenarioDefinition || {});
  const after = summarizeAutopilotEvidence(finalReport, finalOracle, finalReport.scenarioDefinition || {});
  const delta = {
    weightedScore: after.weightedScore - before.weightedScore,
    confidence: after.confidence - before.confidence,
    weakestScore: after.weakestScore - before.weakestScore,
    lifeScore: after.lifeScore - before.lifeScore,
    flatTurnRate: after.flatTurnRate - before.flatTurnRate,
    aliveTurnRate: after.aliveTurnRate - before.aliveTurnRate,
    invalidAttempts: after.invalidAttempts - before.invalidAttempts,
    zeroStatPlayers: after.zeroStatPlayers - before.zeroStatPlayers,
    artifacts: after.artifacts - before.artifacts,
    revealedZones: after.revealedZones - before.revealedZones,
  };
  const rejectedReasons = [];
  if (delta.weightedScore < -2) rejectedReasons.push(`Oracle score regressed by ${Math.abs(delta.weightedScore).toFixed(1)}`);
  if (delta.confidence < -0.05) rejectedReasons.push(`confidence regressed by ${Math.abs(delta.confidence).toFixed(2)}`);
  if (delta.flatTurnRate > 0.03) rejectedReasons.push(`flat-turn rate worsened by ${delta.flatTurnRate.toFixed(2)}`);
  if (delta.invalidAttempts > 1) rejectedReasons.push(`invalid attempts worsened by ${delta.invalidAttempts.toFixed(1)}`);
  if (delta.zeroStatPlayers > 0.25) rejectedReasons.push(`zero-stat players worsened by ${delta.zeroStatPlayers.toFixed(1)}`);
  return {
    before,
    after,
    delta,
    accepted: rejectedReasons.length === 0 && (delta.weightedScore > 0 || delta.confidence > 0.02 || delta.lifeScore > 1),
    rejectedReasons,
  };
}

export function finalAutopilotVerdict({ oracle = null, comparison = null, limits = autopilotLimits() } = {}) {
  if (comparison?.accepted) return 'improved';
  if (comparison && !comparison.accepted) return 'rejected-regression';
  if (!oracle) return 'planned';
  if (oracle.gate && !oracle.gate.passed) return 'blocked';
  if (Number(oracle.weightedScore || 0) >= limits.targetScore && Number(oracle.confidence || 0) >= limits.targetConfidence) return 'target-met';
  if (['strong-pass', 'pass'].includes(oracle.oracleVerdict)) return 'healthy';
  return 'needs-iteration';
}

